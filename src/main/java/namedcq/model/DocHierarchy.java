package namedcq.model;

public class DocHierarchy {

	int docHierarchyId;
	String docHierarchyName;
	String docHierarchyOwner;
	int docDetail;
	
	public int getDocHierarchyId() {
		return docHierarchyId;
	}
	public void setDocHierarchyId(int docHierarchyId) {
		this.docHierarchyId = docHierarchyId;
	}
	public String getDocHierarchyName() {
		return docHierarchyName;
	}
	public void setDocHierarchyName(String docHierarchyName) {
		this.docHierarchyName = docHierarchyName;
	}
	public String getDocHierarchyOwner() {
		return docHierarchyOwner;
	}
	public void setDocHierarchyOwner(String docHierarchyOwner) {
		this.docHierarchyOwner = docHierarchyOwner;
	}
	public int getDocDetail() {
		return docDetail;
	}
	public void setDocDetail(int docDetail) {
		this.docDetail = docDetail;
	}
	
	
	
}
