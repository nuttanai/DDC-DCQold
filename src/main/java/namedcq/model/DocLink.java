package namedcq.model;

public class DocLink {

	int docLinkId;
	int docLinkOwner;
	String docLink;
	
	
	public int getDocLinkId() {
		return docLinkId;
	}
	public void setDocLinkId(int docLinkId) {
		this.docLinkId = docLinkId;
	}
	public int getDocLinkOwner() {
		return docLinkOwner;
	}
	public void setDocLinkOwner(int docLinkOwner) {
		this.docLinkOwner = docLinkOwner;
	}
	public String getDocLink() {
		return docLink;
	}
	public void setDocLink(String docLink) {
		this.docLink = docLink;
	}
	
	
	
}
